/**
 * Convert shortcut configs to RadialMenuAction objects
 */
export function shortcutsToActions(
  shortcuts: RadialMenuShortcutConfig[],
  options: {
    success: (message: string) => void;
    error: (message: string) => void;
    navigate: (path: string) => void;
  }
): RadialMenuAction[] {
  return shortcuts.map((shortcut) => ({
    id: shortcut.id,
    icon: getIconSVG(shortcut.iconType),
    label: shortcut.label,
    disabled: shortcut.disabled,
    onClick: async () => {
      if (shortcut.action.type === 'ha-call') {
        try {
          const response = await fetch('/api/ha/call', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({
              domain: shortcut.action.domain,
              service: shortcut.action.service,
              entity_id: shortcut.action.entity_id,
              ...(shortcut.action.service_data && { service_data: shortcut.action.service_data }),
            }),
          });

          const data = await response.json();
          if (!data.ok) {
            throw new Error(data.error || 'Failed to call service');
          }

          options.success(`${shortcut.label} executed`);
        } catch (err: unknown) {
          console.error(`[RadialMenu] ${shortcut.label} error:`, err);
          options.error(err instanceof Error ? err.message : 'Failed to execute action');
        }
      } else if (shortcut.action.type === 'navigate') {
        options.navigate(shortcut.action.path || '/dashboard');
      } else {
        // Custom action placeholder
        options.success(`${shortcut.label} - coming soon`);
      }
    },
  }));
}
